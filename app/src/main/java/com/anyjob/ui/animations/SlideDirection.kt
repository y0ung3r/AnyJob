package com.anyjob.ui.animations

/**
 * Определяет направление движения для Slide анимации
 */
enum class SlideDirection {
    /**
     * Из центра вверх
     */
    FromCenterToTop,

    /**
     * Сверху в центр
     */
    FromTopToCenter,

    /**
     * Из центра налево
     */
    FromCenterToLeft,

    /**
     * Из центра направо
     */
    FromCenterToRight
}