package com.anyjob.ui.animations.slide

/**
 * Определяет к какой стороне Layout'а необходимо прикрепить View, которая будет анимироваться
 */
enum class SlideFrom {
    /**
     * Сверху
     */
    Top,

    /**
     * Снизу
     */
    Bottom,

    /**
     * Слева
     */
    Left,

    /**
     * Справа
     */
    Right
}